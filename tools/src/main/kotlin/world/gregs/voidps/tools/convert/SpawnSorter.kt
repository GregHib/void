package world.gregs.voidps.tools.convert

import java.io.File

/**
 * Moves item/npc/object spawns into region based files
 */
object SpawnSorter {
    @JvmStatic
    fun main(args: Array<String>) {
        val type = ".teles.toml"
        val file = File("../void/data/objects/teleports/all$type")

        val lines = file.readLines()
        val directory = file.parentFile
        var region = 0
        val files = mutableSetOf<File>()
        for (line in lines) {
            if (line.trim(' ').startsWith("#")) {
                region = line.trim('#', ' ').toInt()
                val file = directory.resolve("${regionName(region) ?: region.toString()}$type")
                if (file.exists()) {
                    append(file, "# $region ${regionName(region) ?: region.toString()}\n")
                } else {
                    file.createNewFile()
                    append(file, "# $region ${regionName(region) ?: region.toString()}\n")
                    files.add(file)
                }
                continue
            }
            val name = regionName(region) ?: region.toString()
            val file = directory.resolve("${name}$type")
            append(file, "${line.trim()}\n")
        }
    }

    private fun append(file: File, text: String) {
        file.appendText(text)
//        print("[${file.name}]$text")
    }

